# A script that excute different lists of different datatypes.
list = ['John', 12, 12.9, 20.98]
tinylist = ['Baba', 'xyz']
print (list + tinylist)   #all data will be printed out;
print (list[1:4])   #data from 2nd element to 4th will be printed;
print (list[3:])   #data starting the 4th one will be printed;
print (list[:2])  #data starting from 1 to 2 will be printed out;